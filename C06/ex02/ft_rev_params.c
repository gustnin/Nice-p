#include <unistd.h>

int		main(int argc, char *argv[])
{
	int i;

	i = 1;
	while (argc > i)
	{
		while (*argv[argc - 1])
			write(1, (argv[argc - 1])++, 1);
		write(1, "\n", 1);
		argc--;
	}
	return (0);
}
