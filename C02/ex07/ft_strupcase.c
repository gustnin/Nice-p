char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		while (str[i] >= 97 && str[i] <= 122)
		{
			str[i] = str[i] - 32;
			i++;
		}
		i++;
	}
	return (str);
}
