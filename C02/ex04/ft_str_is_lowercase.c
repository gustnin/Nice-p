int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		while (str[i] >= 97 && str[i] <= 122)
		{
			i++;
			if (str[i] == '\0')
				return (1);
		}
		if (str[i] < 97 || str[i] > 122)
			return (0);
	}
	return (1);
}
