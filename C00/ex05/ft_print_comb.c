#include <unistd.h>

void	ft_print_comb(void)
{
	char com[5];

	com[0] = '0';
	com[3] = ',';
	com[4] = ' ';
	while (com[0] <= '6')
	{
		com[1] = com[0] + 1;
		while (com[1] <= '8')
		{
			com[2] = com[1] + 1;
			while (com[2] <= '9')
			{
				write(1, &com, 5);
				com[2]++;
			}
			com[1]++;
		}
		com[0]++;
	}
	write(1, "789", 3);
}
