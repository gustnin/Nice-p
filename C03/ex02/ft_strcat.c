char	*ft_strcat(char *dest, char *src)
{
	char	*ptr;
	int		i;
	int		j;

	ptr = dest;
	i = 0;
	j = 0;
	while (ptr[i])
	{
		i++;
	}
	while (src[j])
	{
		ptr[i] = src[j];
		i++;
		j++;
	}
	ptr[i] = 0;
	return (dest);
}
