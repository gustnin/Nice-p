#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb > 0)
	{
		if (nb > 9)
			ft_putnbr(nb / 10);
		nb = nb % 10 + '0';
		write(1, &nb, 1);
	}
	else if (nb == 0)
		write(1, "0", 1);
	else
	{
		write(1, "-", 1);
		nb = -1 * nb;
		ft_putnbr(nb);
	}
}
