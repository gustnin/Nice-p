int		ft_sqrt(int nb)
{
	long long	i;
	long long	j;

	i = 1;
	j = nb;
	if (j <= 0)
		return (0);
	while (i * i < j)
		i++;
	if (i * i == j)
		return (i);
	return (0);
}
